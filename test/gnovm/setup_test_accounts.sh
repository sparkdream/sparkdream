#!/bin/bash

echo "=================================================="
echo "SETUP: Initializing Test Accounts for x/gnovm Tests"
echo "=================================================="
echo ""

# ========================================================================
# Configuration
# ========================================================================
BINARY="sparkdreamd"
CHAIN_ID="sparkdream"
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"

# Get alice address (genesis account with funds)
ALICE_ADDR=$($BINARY keys show alice -a --keyring-backend test)

echo "Deployer (Alice): $ALICE_ADDR"
echo ""

# Delete stale .test_env so it is regenerated
if [ -f "$SCRIPT_DIR/.test_env" ]; then
    echo "Removing stale .test_env (will be regenerated)..."
    rm -f "$SCRIPT_DIR/.test_env"
fi

# ========================================================================
# Helper Functions
# ========================================================================

wait_for_tx() {
    local TXHASH=$1
    local MAX_ATTEMPTS=20
    local ATTEMPT=0

    while [ $ATTEMPT -lt $MAX_ATTEMPTS ]; do
        RESULT=$($BINARY q tx $TXHASH --output json 2>&1)
        if echo "$RESULT" | jq -e '.code' > /dev/null 2>&1; then
            echo "$RESULT"
            return 0
        fi
        ATTEMPT=$((ATTEMPT + 1))
        sleep 1
    done

    echo "ERROR: Transaction $TXHASH not found after $MAX_ATTEMPTS attempts" >&2
    return 1
}

check_tx_success() {
    local TX_RESULT=$1
    local CODE=$(echo "$TX_RESULT" | jq -r '.code')
    if [ "$CODE" != "0" ]; then
        return 1
    fi
    return 0
}

# ========================================================================
# Verify GnoVM params are configured
# ========================================================================
echo "--- Verifying GnoVM params ---"
GNOVM_DOMAIN=$($BINARY query gnovm params --output json 2>&1 | jq -r '.params.chain_domain')
if [ "$GNOVM_DOMAIN" != "gno.land" ]; then
    echo "FAIL: GnoVM chain_domain is '$GNOVM_DOMAIN', expected 'gno.land'"
    exit 1
fi
echo "  chain_domain: $GNOVM_DOMAIN"

SYSNAMES=$($BINARY query gnovm params --output json 2>&1 | jq -r '.params.sysnames_pkgpath // ""')
if [ -n "$SYSNAMES" ]; then
    echo "WARNING: sysnames_pkgpath is set to '$SYSNAMES' — package deployment may require namespace registration"
fi
echo "  sysnames_pkgpath: (empty — open deployment)"
echo ""

# ========================================================================
# Write test environment file
# ========================================================================
echo "--- Writing .test_env ---"
cat > "$SCRIPT_DIR/.test_env" << EOF
# Auto-generated by setup_test_accounts.sh
BINARY="$BINARY"
CHAIN_ID="$CHAIN_ID"
ALICE_ADDR="$ALICE_ADDR"
CONTRACTS_DIR="$SCRIPT_DIR/contracts"
EOF

echo "  Written to $SCRIPT_DIR/.test_env"
echo ""
echo "=================================================="
echo "SETUP COMPLETE"
echo "=================================================="
