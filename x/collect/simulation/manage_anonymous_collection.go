func SimulateMsgManageAnonymousCollection(
	ak types.AuthKeeper,
	bk types.BankKeeper,
	k keeper.Keeper,
	txGen client.TxConfig,
) simtypes.Operation {
	return func(r *rand.Rand, app *baseapp.BaseApp, ctx sdk.Context, accs []simtypes.Account, chainID string,
	) (simtypes.OperationMsg, []simtypes.FutureOperation, error) {
		msg := &types.MsgManageAnonymousCollection{}
		return simtypes.NoOpMsg(types.ModuleName, sdk.MsgTypeURL(msg), "anonymous collection management requires Ed25519 key"), nil, nil
	}
}
